/**
 * Initialize contract instance.
 *
 * @param {Web3} web3Inst - Web3 instance to use
 * @return {Web3.eth.Contract|null} - Contract instance or null on failure
 */
function initContract(web3Inst) {
  if (!web3Inst) {
    console.error("No web3 instance provided to initialize contract");
    return null;
  }

  const contract = new web3Inst.eth.Contract(CONTRACT_ABI, CONTRACT_ADDRESS);
  console.log("Contract initialized");
  return contract;
}

